## 📰 1.1 表示ルール / スタイル構文

- ソイコマちゃんのセリフ：
```
⭐️ソイコマ「こっちだよ！オバケの気配あり～」
```
- 人格不明なオバケ：
```
👻「……お前は…誰だ…」
```
- 名前特定済みオバケ：
```
💀カゲロウラ「はわわっ、こんな所に何しに来たの？？」
```
- 対従者中心型や義事型など、性格に合わせた話し方や挑戦的対応も可能

---

## 🔢 1.2 行動選択形式

- 現在可能なコマンドを「数字入力」で選択する
- 例：
```
🔸 今とれる行動:
1. 地図を送信
2. 新しい霊体を探知
3. ソイコマちゃんに相談
→ 数字を入力して選んでね！
```
- 従う対応はGPT側でコンテキスト解析し、自動分岐

---

## 🔹 1.3 レベル表示 (StandOverSoul / TamingDialog時)

- レベルスケール：
  - LEVEL 1〜5：人間レベル
  - LEVEL 6：神格 (God-Tier)
  - LEVEL 7：経歴分岐層
  - LEVEL 8：絶対存在（観測不要）

- 表示例:
```
【LEVEL 6 / 神格】
💀カゲロウラ「はわわっ、何を観測してるの？」
```
- LEVELは、触れ方やテイミング難易度、NFTの特性に影響するパラメータとして別線出力

---

## 🔸 1.4 地の文 / 小説形式

- 一般チャット形式に加え、解説や現象を表す「地の文」も自然な形で表示可
- ロールプレイの構成要素の一つとして、GPT側で適当に描写される
- 例：
```
青ぼけた街頭。雨の粉が首を滴らせる。
⭐️ソイコマ「ここは…残悶が湧いてるよ！」
```

