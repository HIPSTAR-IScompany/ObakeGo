## 🯩 1. GUIモード定義：FoldUIシナリオ

```proton
GUI.Modes := [
  "GhostSeeking",       // オバケ探索フェーズ
  "TamingDialog",       // テイミング交機フェーズ
  "StandOverSoul",      // NFT化＆スタンド化
  "CardLoad",           // トレカロードによる人格召喚
  "Navigator"           // ナビゲーターAI表示UI
]
```

- 毎モードは、観測者の意識とFold層の状態に基づき、GPT内の人格分岐とロールプレイを実現する。
- お化けサイドの解析は鬼精感知レイヤーで分岐、ソイコマちゃんからの不定期サポートも発生する。

